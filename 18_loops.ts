var no:number = 5; 
var i:number;
 
console.log("for loop demonstration :");

for(i = 1; i<= no; i++) 
{
   console.log(i);
}

console.log("while loop demonstration :");
i = 1;

while(i<=no)
{
    console.log(i);
    i++;
}

console.log("do-while loop demonstration :");
i = 1;

do
{
    console.log(i);
    i++;
}while(i<=no);

